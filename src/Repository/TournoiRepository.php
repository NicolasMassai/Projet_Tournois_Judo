    public function findByUserParticipation($user)
    {
        return $this->createQueryBuilder('t')
            ->join('t.combattant', 'a')  // Utilisation de 'combattant' au lieu de 'adherant'
            ->where('a.id = :userId')
            ->setParameter('userId', $user->getId())
            ->getQuery()
            ->getResult();
